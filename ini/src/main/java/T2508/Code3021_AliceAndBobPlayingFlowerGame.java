public class Code3021_AliceAndBobPlayingFlowerGame {

    /**
     * 计算花朵游戏的结果
     *
     * @param n 游戏参数n
     * @param m 游戏参数m
     * @return 返回n和m乘积的一半
     */
    public long flowerGame(int n, int m) {
        return (long) n * m / 2;
    }

}
